public class Lead {

	private String nome;
	private String email;
	private BusinessTo bTo;
	
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public BusinessTo getbTo() {
		return bTo;
	}
	public void setbTo(BusinessTo bTo) {
		this.bTo = bTo;
	}
	
	
	


}



